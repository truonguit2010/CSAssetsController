- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath {

    if (indexPath.row == 0) {
        BannerTableViewCell* cell = [tableView dequeueReusableCellWithIdentifier:BANNER_TABLE_VIEW_CELL_ID forIndexPath:indexPath];
        return cell;
    }
    if (indexPath.row == 1) {
        ProductTableViewCell* cell = [tableView dequeueReusableCellWithIdentifier:PRODUCT_TABLE_VIEW_CELL_ID forIndexPath:indexPath];
        return cell;
    }
    if (indexPath.row == 2) {
        DisCoverStoreTableViewCell *cell = [tableView dequeueReusableCellWithIdentifier:DISCOVER_STORE_VIEW_CELL_ID forIndexPath:indexPath];
        // Cell Store
        return cell;
    }
    return nil;
}


